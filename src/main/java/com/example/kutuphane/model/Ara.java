package com.example.kutuphane.model;

public class Ara {
    private Integer i;
    private String text;

    public Ara() {
    }

    public Ara(Integer i, String text) {
        this.i = i;
        this.text = text;
    }

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

}
